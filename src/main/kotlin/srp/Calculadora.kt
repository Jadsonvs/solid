package org.example.srp

class Calculadora {

    fun somar(num1: Int, num2: Int): Int {
        return num1 + num2
    }

    fun subtrair(num1: Int, num2: Int): Int {
        return num1 - num2
    }

    fun multiplicar(num1: Int, num2: Int): Int {
        return num1 * num2
    }

    fun dividir(num1: Int, num2: Int): Int {
        return num1 / num2
    }
}